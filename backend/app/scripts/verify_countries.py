for country in countries:
    name = DATASET_NAME_MAPPING.get(country["name"], country["name"])
    dataset_countries.add(name)

missing = sorted(REQUIRED_COUNTRIES - dataset_countries)

extra = sorted(dataset_countries - REQUIRED_COUNTRIES)

print(f"Required: {len(REQUIRED_COUNTRIES)}")
print(f"Dataset : {len(dataset_countries)}")

print("\nMissing Countries")
print("-" * 50)

if not missing:
    print("None")
else:
    for country in missing:
        print(country)

print("\nExtra Countries")
print("-" * 50)

if not extra:
    print("None")
else:
    for country in extra:
        print(country)

print(
    f"\nMatched: "
    f"{len(REQUIRED_COUNTRIES) - len(missing)}"
    f"/{len(REQUIRED_COUNTRIES)}"
)
